
public class Operators {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;

        System.out.println("Addition Operators: " + (a + b));
        System.out.println("Subtraction Operators: " + (a - b));
        System.out.println("Multiply Operators: " + (a * b));
        System.out.println("division Operators: " + (float)(a / b));
        System.out.println("pre-increment Operators: " + (++a));
        System.out.println("pre-decrement Operators: " + (--a));
        System.out.println("post-increment Operators: " + (a++));
        System.out.println("post-decrement Operators: " + (a--));
}
}