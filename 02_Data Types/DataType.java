import java.util.*;

public class DataType{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter Integer Number: ");
        int a = sc.nextInt();
        System.out.print("Number is: " + a);
        System.out.println();
        System.out.print("Enter Byte Number: ");
        byte b = sc.nextByte();
        System.out.print("Number is: " + b);
        System.out.println();
        System.out.print("Enter Float Number: ");
        float c = sc.nextFloat();
        System.out.print("Number is: " + c);
        System.out.println();
        System.out.print("Enter Double Number: ");
        double d = sc.nextDouble();
        System.out.println("Number is: " + d);
        System.out.println();
        System.out.print("Enter Character: ");
        String e = sc.next();
        System.out.println("Character is: " + e);
        System.out.println();
        System.out.print("Enter Line: ");
        String f = sc.nextLine();
        System.out.println("Character is: " + f);
        System.out.println();
        System.out.print("Enter Byte Number: ");
        int g = sc.nextByte();
        System.out.print("Byte Number is: " + g);
        System.out.println();
    }
}
